function update() {
var element = document.getElementById("myprogressBar");
var width = 1;
var identity = setInterval(scene, 15);
function scene() {
	if (width >= 95) {
	clearInterval(identity);
	} else {
	width++;
	element.style.width = width + '%';
	element.innerHTML = width * 1 + '%';
	}
}
}

